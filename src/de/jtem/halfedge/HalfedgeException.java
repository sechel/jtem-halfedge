package de.jtem.halfedge;

public class HalfedgeException extends Exception {

	private static final long serialVersionUID = 1L;

	public HalfedgeException() {
		super();
	}

	public HalfedgeException(String message, Throwable cause) {
		super(message, cause);
	}

	public HalfedgeException(String message) {
		super(message);
	}

	public HalfedgeException(Throwable cause) {
		super(cause);
	}
	
}
